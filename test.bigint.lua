local bigint = require "bigint"

local x

x = bigint("123456789012345678901234567890")
assert(type(x) == "table")
assert(x:tostring() == "123456789012345678901234567890")

x = bigint( ("123456789012345678901234567890"):rep(5) )
print(x:tostring())
print("10^"..(#x.v * x.size)) -- 10^150

